#ifndef __RTC_h__ //内部接口
#define __RTC_h__

//系统时间结构
typedef  struct
{
  uint16   chYear;                // 年
  uint8    chMonth;             // 月
  uint8    chDay;                 // 日
  uint8    chHour;                // 时
  uint8    chMinute;            // 分
  uint8    chSecond;            // 秒
  uint8    chWeek;                // 周
} SYSTIME;

#define INTER_RTC     0x00      //内部时钟
#define EXTER_RTC     0x01      //外部时钟



/*
*******************************************************************************************************
** 函数名称 ：SetRTCType()
** 函数功能 ：设置时钟类型
** 入口参数 ：type类型:INTER_RTC:0 内部时钟 , EXTER_RTC:1 外部时钟
** 出口参数 ：无
*******************************************************************************************************
*/
extern void     SetRTCType(uint8 type);

/*
*******************************************************************************************************
** 函数名称 ：RTCInit()
** 函数功能 ：初始化实时时钟
** 入口参数 ：无
** 出口参数 ：无
*******************************************************************************************************
*/
extern void     RTCInit(void);

/*
*******************************************************************************************************
** 函数名称 ：SetTime()
** 函数功能 ：设置时钟
** 入口参数 ：sTime 要设置的时间
** 出口参数 ：
*******************************************************************************************************
*/
extern uint8    SetTime(SYSTIME *sTime);

/*
*******************************************************************************************************
** 函数名称 ：GetTime()
** 函数功能 ：获取时钟
** 入口参数 ：sTime 获得的时间
** 出口参数 ：
*******************************************************************************************************
*/
extern uint8   GetTime(SYSTIME *sTime);

/*
*******************************************************************************************************
** 函数名称 ：IntiAlarmTimer()
** 函数功能 ：初始化RTC的报警时间值。
** 入口参数 ：无
** 出口参数 ：无
*******************************************************************************************************
*/
extern void     IntiAlarmTimer(void);

/*
*******************************************************************************************************
** 函数名称 ：IntiAlarmTimer()
** 函数功能 ：设置RTC的报警时间值。
** 入口参数 ：sTime 要设置的时间
** 出口参数 ：
*******************************************************************************************************
*/
extern uint8  SetAlarmTime(uint8 *sTime);

/*
*******************************************************************************************************
** 函数名称 ：CheckAlarmTimeAndReset()
** 函数功能 ：检测是否重启
** 入口参数 ：sTimeNow 与SetAlarmTime所设置的时间相比较,如果相等就重启终端
** 出口参数 ：无
*******************************************************************************************************
*/
extern void     CheckAlarmTimeAndReset(SYSTIME sTimeNow);

/*
*******************************************************************************************************
** 函数名称 ：IntiWatchDog()
** 函数功能 ：初始化看门狗
** 入口参数 ：无
** 出口参数 ：无
*******************************************************************************************************
*/
extern void     InitWatchDog(void);

/*
*******************************************************************************************************
** 函数名称 ：GetTick()
** 函数功能 ：取当前秒数
** 入口参数 ：无
** 出口参数 ：当前秒数
*******************************************************************************************************
*/
extern uint8    GetTick(void);

/*
*******************************************************************************************************
** 函数名称 ：GetPause()
** 函数功能 ：取的时间间隔（单位：秒）
** 入口参数 ：cStartTick 开始时间
** 出口参数 ：时间间隔
*******************************************************************************************************
*/

extern uint8    GetPause(uint8  cStartTick);

/*
*******************************************************************************************************
** 函数名称 ：GetTickCount()
** 函数功能 ：取当前毫秒数
** 入口参数 ：无
** 出口参数 ：毫秒数
*******************************************************************************************************
*/
extern uint32  GetTickCount(void);


#endif
