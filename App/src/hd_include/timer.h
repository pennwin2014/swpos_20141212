#ifndef __TIMER_H__
#define __TIMER_H__

typedef void (*OnTimer)(uint8 cTimerID);

/*
*********************************************************************************************************
** 函数名称 ：Timer_Init()
** 函数功能 ：定时器初始化
** 入口参数 ：无
** 出口参数 ：无
*********************************************************************************************************
*/
void InitTimer(void);
/*
*********************************************************************************************************
** 函数名称 ：SetTimer()
** 函数功能 ：设置定时器处理服务程序
** 入口参数 ：uint8 nID 定时器id号，
			: uint32 lMillisecond 定时器定时间，
			: bRepeat 是否周期性执行还是一次性执行，true 为周期性执行，false 为一次性执行
** 出口参数 ：无
*********************************************************************************************************
*/
void SetTimer(uint8 cID,uint32 lMillisecond,uint8 bRepeat,OnTimer pfunc);

/*
*********************************************************************************************************
** 函数名称 ：KillTimer()
** 函数功能 ：关闭定时器
** 入口参数 ：uint8 nID 定时器id号
** 出口参数 ：无
*********************************************************************************************************
*/
void KillTimer(uint8 nID);
#endif
